/**
 * Body Diagram 해부학 도형 데이터
 *
 * - viewBox 0 0 500 950 기준. 화면 왼쪽 절반만 저작하고 오른쪽은 미러 그룹
 *   (translate(500,0) scale(-1,1))으로 렌더링해 좌우 대칭을 보장한다.
 * - ANTERIOR(전면): 화면 왼쪽 = 환자 우측 → direct = "우측 X", mirror = "좌측 X"
 * - POSTERIOR(후면): 화면 왼쪽 = 환자 좌측 → direct = "좌측 X", mirror = "우측 X"
 * - 곡선은 점 목록을 Catmull-Rom → cubic bezier로 스무딩(smoothPath)해 생성.
 */

export type Pt = [number, number];

interface ShapeBase {
  deep?: boolean; // 심부 근육 (점선 표시)
  bone?: boolean; // 뼈·랜드마크 (밝은 톤)
}

export type DiagramShape = ShapeBase &
  ({ kind?: undefined; pts: Pt[] } | { kind: "circle"; cx: number; cy: number; r: number });

export type CenterShape = DiagramShape & { name: string };
export type PairedShape = DiagramShape & { base: string };

/* ── Catmull-Rom → cubic bezier 스무딩 ── */
export function smoothPath(pts: Pt[], closed = true, tension = 1): string {
  const n = pts.length;
  if (n < 3) return "";
  let d = `M ${pts[0][0]},${pts[0][1]} `;
  const last = closed ? n : n - 1;
  for (let i = 0; i < last; i++) {
    const p0 = pts[closed ? (i - 1 + n) % n : Math.max(i - 1, 0)];
    const p1 = pts[i];
    const p2 = pts[(i + 1) % n];
    const p3 = pts[closed ? (i + 2) % n : Math.min(i + 2, n - 1)];
    const c1 = [p1[0] + ((p2[0] - p0[0]) / 6) * tension, p1[1] + ((p2[1] - p0[1]) / 6) * tension];
    const c2 = [p2[0] - ((p3[0] - p1[0]) / 6) * tension, p2[1] - ((p3[1] - p1[1]) / 6) * tension];
    d += `C ${c1[0].toFixed(1)},${c1[1].toFixed(1)} ${c2[0].toFixed(1)},${c2[1].toFixed(1)} ${p2[0]},${p2[1]} `;
  }
  return d + (closed ? "Z" : "");
}

const rect4 = (x1: number, y1: number, x2: number, y2: number): Pt[] => [
  [x1, y1], [x2, y1], [x2, y2], [x1, y2],
];

